int main(void)
{

  // Initialize device
  initMcu();
  // Initialize board
  initBoard();
  // Initialize application
  initApp();

  // Initialize stack
  gecko_init(&config);

  while (1) {
	/* Event pointer for handling events */
	struct gecko_cmd_packet* evt;

	/* Check for stack event. */
	evt = gecko_wait_event();

	gecko_update(evt);
  }
}
